% Resevanje testnega primera 3x3 z Newtonovo metodo
% Bor Plestenjak 2004

F=inline(strcat(...
   '[ 3*x(1) - cos(x(1)*x(2)) - 0.6;',...
   '  x(1)^2 - 81*(x(2)+0.1)^2 + sin(x(3)) + 1.1;',...
   '  exp(-x(1)*x(2)) + 20*x(3) + 9.1]'))

% Jacobijeva matrika

JF=inline(strcat(...
   '[ 3 + x(2)*sin(x(1)*x(2)), x(1)*sin(x(1)*x(2)),   0;',...    
   '  2*x(1),                  -162*(x(2)+0.1),       cos(x(3));',... 
   '  -x(2)*exp(-x(1)*x(2)),   -x(1)*exp(-x(1)*x(2)), 20]'));

newton(F,JF,[0.4;0.1;-0.4])
